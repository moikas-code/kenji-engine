  static getLogoForWidth(width: number): string {
    if (width >= 120) {
      return this.KENJI_LOGO;
    } else if (width >= 80) {
      return this.KENJI_COMPACT;
    } else {
      return this.KENJI_MINI;
    }
  }

  static centerText(text: string, width: number): string {
    const lines = text.split("\n");
    return lines
      .map((line) => {
        const padding = Math.max(0, Math.floor((width - line.length) / 2));
        return " ".repeat(padding) + line;
      })
      .join("\n");
  }
